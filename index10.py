class BankAccount:
    def __init__(self, owner, balance=0):
        self.owner = owner
        self.balance = balance

    def deposit(self, amount): # Пополнение денег
        if amount <= 0:
            print("Сумма должна быть положительной")
            return
        self.balance += amount
        print(f"{self.owner} пополнил счет на {amount} сом, теперь у вас {self.balance} сом")

    def withdraw(self, amount): # снятие денег
        if amount > self.balance:
            print("Недостаточно средств")
            return
        self.balance -= amount
        print(f"{self.owner} снял со счета {amount} сом, теперь у вас {self.balance} сом")

    def info(self):
        print(f"Владелец: {self.owner}, Счет: {self.balance}")


class Bank:
    def __init__(self, name):
        self.name = name # Имя банка
        self.accounts = []

    def open_account(self, owner): # создаёт новый счёт
        account = BankAccount(owner)
        self.accounts.append(account)
        print(f"Открыт счет на имя {owner} в банке {self.name}")
        return account
    
    def find_account(self, owner): # возврощает счёт по имени
        for acc in self.accounts:
            if acc.owner == owner:
                return acc
            print(f"Нe найден {owner}")
            return None

        def transfer(self, from_owner, to_owner, amount):
            from_acc = self.find_account(from_owner)
            to_acc = self.find_account(to_owner)
            if from_acc or not to_acc:
                print("перевод невозможен")
                return
            if from_acc.balance < amount:
                print("Недостаточно средств")
                return
            from_acc.withdraw(amount)
            to_acc.deposit(amount)
            print(f"перевод {amount} сом от {from_owner} к {to_owner} успешно выполнен")

