def toy_store(toys):
    cart = {}
    total = 0 

    while True:
        toy_name = input("Введите название игрушки (или 'стоп' для завершения): ").strip()
        toy_key = None

        for key in toys:
            if key.lower() == toy_name.lower():
                toy_key = key
                break

        if toy_name.lower() == 'стоп':
            break

        if toy_key is None:
            print("Такой игрушки нет")
            continue

        if toys[toy_key]["Количество"] == 0:
            print("Этой игрушки нет в наличии")
            continue

        try:
            qty = int(input(f"Сколько штук '{toy_key}' вы хотите купить? "))
        except ValueError:
            print("Введите корректное число")
            continue

        if qty <= 0:
            print("Количество должно быть положительным числом")
            continue

        if qty > toys[toy_key]["Количество"]:
            print("Недостаточно на складе")
            continue

